#include "libft.h"

int		ft_nbr_w(char const *s, char c)
{
	int	i;
	int	nbr_w;
	int	a;

	a = 0;
	nbr_w = 0;
	i = 0;
	while (s[i] != '\0')
	{
		if (s[i] != c && a == 0)
		{
			nbr_w++;
			a = 1;
		}
		if (s[i] == c)
			a = 0;
		i++;
	}
	return (nbr_w);
}
